package Inheeritance;

public class multipleininhertiance {

		interface Animal {
			void walk();
			}
			class Horse implements Animal {
			public void walk() {
			System.out.println("Horse walks on 4 legs");
			}
			}
			class Chicken implements Animal {
			public void walk() {
			System.out.println("Chicken walks on 2 legs");
			}
			}}

